package widening;

public class Widening {
    public static void main(String[] args){

        int number =10;
        double number2 = (double)number;
        System.out.println("Int           : "+number);
        System.out.println("Double        : "+number2);
    }
}
/*

    Widening Casting
    Byte -> Short -> Char -> Int -> Long -> Float -> Double

*/